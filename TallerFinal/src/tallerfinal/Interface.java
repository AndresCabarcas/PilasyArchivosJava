    public void borrartabla() {
        DefaultTableModel modelo = (DefaultTableModel) Tabla.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }

    }

    public void actualizar() throws IOException {

        borrartabla();
        if (m.Contador() > 0) {
            for (int i = 0; i < m.Contador(); i++) {
                //Asignar vista a la tabla
                String fila[] = new String[3];
                fila[0] = m.qCarro(i).getMarca();
                fila[1] = m.qCarro(i).getPlaca();
                fila[2] = m.qCarro(i).getTipo();
                n.addRow(fila);
            }
            Tabla.setModel(n);
            //Guardado en Archivos
            Archivo arc = new Archivo();
            arc.guardarArchivo(m);
            arc.guardarArchivo2(m);
            arc.GuardarMensajes(m);
            JOptionPane.showMessageDialog(null, "Grabado");
        }

        b4.setText("Vacio:   " + m.coco());
        b3.setText("Elementos:  " + m.Contador() + "/20");
        if (m.Contador() == 0) {
            b1.setText("Primero:  no hay");
            b2.setText("Ultimo:  no hay");
        } else {

            b1.setText("Primero:  " + m.Primero().getPlaca());
            b2.setText("Ultimo: " + m.Ultimo().getPlaca());
        }
    }


