    private void BotonPopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonPopActionPerformed
// este metodo solo sacara el auto en el tope 
        if (m.Contador() > 0) {
            m.Pop();

            int valor = 0;
            int horas = Integer.parseInt(JOptionPane.showInputDialog("Cantidad de horas"));
            String Tipo = JOptionPane.showInputDialog("Digite tipo de carro");
            if (Tipo.equals("Auto") || Tipo.equals("auto")) {
                valor = horas * 2300;
            } else {
                if (Tipo.equals("Camioneta") || Tipo.equals("camioneta")) {
                    valor = horas * 3500;
                } else {
                    if (Tipo.equals("Bus") || Tipo.equals("bus")) {
                        valor = horas * 5000;
                    } else {
                        if (Tipo.equals("Camion") || Tipo.equals("camion")) {
                            valor = horas * 7000;
                        }
                    }
                }
            }
            JOptionPane.showMessageDialog(null, "Su total a pagar es: " + valor);
            JOptionPane.showMessageDialog(null, "Ha salido un auto");
            try {
                actualizar();
            } catch (IOException ex) {
                Logger.getLogger(Interface.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_BotonPopActionPerformed

